-- Create tables for TCM Prescription System

-- Medicines table
CREATE TABLE IF NOT EXISTS medicines (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  code TEXT,
  price DECIMAL(10,2) NOT NULL DEFAULT 0,
  unit TEXT DEFAULT 'g',
  dosage DECIMAL(10,2) DEFAULT 10,
  category TEXT,
  created_at TIMESTAMP DEFAULT NOW()
);

-- Prescriptions table
CREATE TABLE IF NOT EXISTS prescriptions (
  id SERIAL PRIMARY KEY,
  prescription_no TEXT NOT NULL,
  patient_name TEXT NOT NULL,
  patient_gender TEXT DEFAULT '男',
  patient_age TEXT,
  patient_phone TEXT,
  patient_address TEXT,
  diagnosis TEXT,
  total_amount DECIMAL(10,2) DEFAULT 0,
  medical_fee DECIMAL(10,2) DEFAULT 0,
  dose_count INT DEFAULT 7,
  created_at TIMESTAMP DEFAULT NOW()
);

-- Prescription items table
CREATE TABLE IF NOT EXISTS prescription_items (
  id SERIAL PRIMARY KEY,
  prescription_id INT REFERENCES prescriptions(id) ON DELETE CASCADE,
  medicine_name TEXT NOT NULL,
  medicine_code TEXT,
  dosage DECIMAL(10,2) DEFAULT 10,
  unit TEXT DEFAULT 'g',
  price DECIMAL(10,2) DEFAULT 0,
  total DECIMAL(10,2) DEFAULT 0
);

-- Formulas (tested recipes) table
CREATE TABLE IF NOT EXISTS formulas (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  code TEXT,
  effect TEXT,
  indication TEXT,
  created_at TIMESTAMP DEFAULT NOW()
);

-- Formula compositions table
CREATE TABLE IF NOT EXISTS formula_compositions (
  id SERIAL PRIMARY KEY,
  formula_id INT REFERENCES formulas(id) ON DELETE CASCADE,
  medicine_name TEXT NOT NULL,
  dosage DECIMAL(10,2) DEFAULT 10,
  unit TEXT DEFAULT 'g'
);

-- Users table
CREATE TABLE IF NOT EXISTS users (
  id SERIAL PRIMARY KEY,
  username TEXT UNIQUE NOT NULL,
  password TEXT NOT NULL,
  name TEXT NOT NULL,
  role TEXT DEFAULT 'doctor',
  created_at TIMESTAMP DEFAULT NOW()
);

-- Create indexes for better performance
CREATE INDEX IF NOT EXISTS idx_medicines_name ON medicines(name);
CREATE INDEX IF NOT EXISTS idx_medicines_code ON medicines(code);
CREATE INDEX IF NOT EXISTS idx_prescriptions_patient ON prescriptions(patient_name);
CREATE INDEX IF NOT EXISTS idx_prescriptions_date ON prescriptions(created_at);
CREATE INDEX IF NOT EXISTS idx_formulas_name ON formulas(name);
CREATE INDEX IF NOT EXISTS idx_users_username ON users(username);

